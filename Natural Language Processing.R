install.packages('tm')         # an exclusive NATURAL LANGUAGE PROCESSING(NLP) package
install.packages('SnowballC')  # an exclusive NATURAL LANGUAGE PROCESSING(NLP) package
install.packages('caTools')
install.packages('randomForest')


#################################################################################################################################################################
#################################### II> LOAD THE LIBRARIES #####################################################################################################
#################################################################################################################################################################

library(tm)                   # an exclusive NATURAL LANGUAGE PROCESSING(NLP) library
library(SnowballC)            # an exclusive NATURAL LANGUAGE PROCESSING(NLP) library
library(caTools)
library(randomForest)
set.seed(123)

#################################################################################################################################################################
#################################### III> IMPORT THE DATASET  ###################################################################################################
#################################################################################################################################################################

dataset_original = read.delim('E:\\DESK PROJECTS\\Github\\DATASETS\\Restaurant_Reviews.tsv', quote = '', stringsAsFactors = FALSE)
summary(dataset_original)

#################################################################################################################################################################
################################### IV> CLEANING THE TEXTS ######################################################################################################
#################################################################################################################################################################

corpus = VCorpus(VectorSource(dataset_original$Review))
corpus = tm_map(corpus, content_transformer(tolower))
corpus = tm_map(corpus, removeNumbers)
corpus = tm_map(corpus, removePunctuation)
corpus = tm_map(corpus, removeWords, stopwords())
corpus = tm_map(corpus, stemDocument)
corpus = tm_map(corpus, stripWhitespace)
summary(corpus)

#################################################################################################################################################################
################################### V> CREATE A "BAG OF WORDS" MODEL ############################################################################################
#################################################################################################################################################################

dtm = DocumentTermMatrix(corpus)
dtm = removeSparseTerms(dtm, 0.999)
dataset = as.data.frame(as.matrix(dtm))
dataset$Liked = dataset_original$Liked
summary(dataset)

#################################################################################################################################################################
#################################### VI> RUN A CLASSIFICATION MODEL AS A PART OF TESTING THE NLP MODEL THAT'S CREATED  ##########################################
#################################################################################################################################################################

#################################### VI-1> Import the dataset ###################################################################################################

dataset = read.csv('E:\\DESK PROJECTS\\Github\\DATASETS\\Social_Network_Ads.csv')
dataset = dataset[3:5]
summary(dataset)

#################################### VI-2> Encode the target feature as a vector  ###############################################################################

dataset$Liked = factor(dataset$Liked, levels = c(0, 1))

#################################### VI-3> Split the dataset as Train:Test  #####################################################################################

split = sample.split(dataset$Liked, SplitRatio = 0.8)
training_set = subset(dataset, split == TRUE)
test_set = subset(dataset, split == FALSE)

#################################### VI-4> Fit the Classification model to the training set  ####################################################################

classifier = randomForest(x = training_set[-692], y = training_set$Liked, ntree = 10)

#################################### VI-5> Predict the test set results  ########################################################################################

y_pred = predict(classifier, newdata = test_set[-692])

#################################### VI-6> Confusion Matrix  ####################################################################################################

cm = table(test_set[, 692], y_pred)


